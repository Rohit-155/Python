# Complement operator (~)

from ast import operator


a = 12
print(bin(a))

# Binary to Integer

a = 0b0011
print(int(a))

# And Operator (&)

a = 12 & 13 
print(a)

# Or Operator (|)

a = 12 | 13
print(a)

# XOR (^)

a = 12^13
print(a)

# Left Shift Operator (<<)

a = 10<<2
print(a)

# Right Shift operator

a = 10>>2
print(a)